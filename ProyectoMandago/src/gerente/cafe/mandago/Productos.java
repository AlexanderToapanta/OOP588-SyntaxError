    private void btEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btEliminarActionPerformed
        // TODO add your handling code here:
        if(filaSeleccion >= 0){
            int confirmacion = JOptionPane.showConfirmDialog(null, "Seguro que desea eliminar los productos?", "Confirmacion", JOptionPane.YES_NO_OPTION);
            if(confirmacion == JOptionPane.YES_OPTION){
                DefaultTableModel modeloTabla = (DefaultTableModel) tblProductos.getModel();
                MongoCollection colleccion = database.getCollection("datosEstudiantes");
                Document filtro = new Document("_id", new ObjectId(txtId.getText()));
                DeleteResult result = colleccion.deleteOne(filtro);

                if(result.getDeletedCount()>0){
                    JOptionPane.showMessageDialog(null, "Registro eliminado correctamente.");
                    modeloTabla.removeRow(filaSeleccion);
                }else{
                    JOptionPane.showMessageDialog(null, "No se encontro el registro para eliminar");
                }
            }else{
                ListSelectionModel seleccionModel = tblProductos.getSelectionModel();
                seleccionModel.clearSelection();
                filaSeleccion = -1;
            }
            limpiar();

            btAgregar.setVisible(true);
        }else{
            JOptionPane.showMessageDialog(null, "Seleccione el registro a eliminar");
        }
    }//GEN-LAST:event_btEliminarActionPerformed

