    public void mostrarDatosTabla(){
        DefaultTableModel modeloTabla = (DefaultTableModel) tblProductos.getModel();
        modeloTabla.setRowCount(0);
        
        MongoCollection<org.bson.Document> collection = database.getCollection("Productos");
        FindIterable<org.bson.Document> documents = collection.find();
        
        
        for(org.bson.Document document : documents){
            Object id = document.get("_id");
            String producto = document.getString("producto");
            double precio = document.getDouble("precio");
            int cantidad = document.getInteger( "cantidad");
            double total = precio*cantidad;
            modeloTabla.addRow(new Object[]{id,producto,precio,cantidad,total});
        }
        
        TableColumnModel columnModel = tblProductos.getColumnModel();
        TableColumn columna = columnModel.getColumn(0);
        columna.setWidth(0);
        columna.setMaxWidth(0);
    }
    
    private void mostrarDatosCampos(){
        filaSeleccion = tblProductos.getSelectedRow();
        if(filaSeleccion == -1){
            return;
        }
        DefaultTableModel modeloTabla = (DefaultTableModel) tblProductos.getModel();
        txtId.setText(modeloTabla.getValueAt(filaSeleccion, 0).toString());
        txtProducto.setText(modeloTabla.getValueAt(filaSeleccion, 1).toString());
        txtPrecio.setText(modeloTabla.getValueAt(filaSeleccion, 2).toString());
        spCantidad.setValue(modeloTabla.getValueAt(filaSeleccion, 5));
        
      
    }

